# 2. ฟังก์ชันจำลองมุมดวงอาทิตย์ของไทย (ตัวอย่างการจำลองอย่างง่าย)
def get_solar_position(hour):
    # สมมุติตอนเที่ยงดวงอาทิตย์อ้อมใต้เล็กน้อยตามตำแหน่งของไทย
    # ในการใช้งานจริง แนะนำให้ใช้ไลบรารี pvlib 
    if 6 <= hour <= 18:
        altitude = 90 - abs(12 - hour) * 10  # มุมเงยจำลอง
        azimuth = 90 + (hour - 6) * 15       # มุมทิศจากตะวันออกไปตะวันตก
        return np.radians(altitude), np.radians(azimuth)
    return 0, 0

# 3. คำนวณการปรับเชือกเพื่อให้ได้เงาดีที่สุด (Optimization)
def optimize_roof(hour):
    alt, az = get_solar_position(hour)
    
    if alt == 0:
        return "พระอาทิตย์ตกดินแล้ว"
        
    # ทิศทางแสงดวงอาทิตย์ (Vector)
    sun_vector = np.array([
        np.cos(alt) * np.sin(az),
        np.cos(alt) * np.cos(az),
        -np.sin(alt)
    ])
    
    # อัลกอริทึมจะคำนวณหาค่า d1, d2, d3, d4 (ระยะผูกเชือกต่ำลงมา)
    # เพื่อให้ระนาบหลังคาเอียงไปบังแดดให้ตกลงกลางพื้นที่เสามากที่สุด
    # สมมุติผลลัพธ์คำนวณที่เหมาะสมที่สุด ณ เวลานั้น:
    d1 = max(0, int(50 * np.cos(az))) # ผูกต่ำลงมากี่ ซม. (มุมที่ 1)
